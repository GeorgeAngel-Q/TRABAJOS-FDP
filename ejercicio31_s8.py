

#Ejercicio 31 extra : Conversión en mayúsculas, minúsculas y otros


mens=" caMaroN quE se DueRmE se Lo lLeva la Corriente "
#Usando "Capitalize": mayúscula
print ("*"*100)
print ("Usando \"capitalize\"")
mens=mens.capitalize()
print (mens)
print ("*"*100)
#Usando "upper": superior
print ("Usando \"upper\"")
mens=mens.upper()
print (mens)
print ("*"*100)
#Usando "lower": inferior
print ("Usando \"lower\"")
mens=mens.lower()
print(mens)
print ("*"*100)
#Usando "swapcase": Invertir
print ("Usando \"swapcase\" ")
mens1=" se DueRmE se Lo lLeva la Corriente"
mens1=mens1.swapcase()
print (mens1)
